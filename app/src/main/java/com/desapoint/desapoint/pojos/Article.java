public class Article {

    private int id;
    private String subject;
    private String article_name;
    private String article_notes;
    private String article_file;
    private String date;
    private String status;
    private String writter;
    private String university;
    private String user_name;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getSubject() {
        return subject;
    }

    public void setSubject(String subject) {
        this.subject = subject;
    }

    public String getArticle_name() {
        return article_name;
    }

    public void setArticle_name(String article_name) {
        this.article_name = article_name;
    }

    public String getArticle_notes() {
        return article_notes;
    }

    public void setArticle_notes(String article_notes) {
        this.article_notes = article_notes;
    }

    public String getArticle_file() {
        return article_file;
    }

    public void setArticle_file(String article_file) {
        this.article_file = article_file;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getWritter() {
        return writter;
    }

    public void setWritter(String writter) {
        this.writter = writter;
    }

    public String getUniversity() {
        return university;
    }

    public void setUniversity(String university) {
        this.university = university;
    }

    public String getUser_name() {
        return user_name;
    }

    public void setUser_name(String user_name) {
        this.user_name = user_name;
    }
}
